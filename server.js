var player = {
    posX: 14,
    posY: 6
}
var SOCKET_LIST = {};
io.on('connection', socket =>{
    SOCKET_LIST[socket.id] = socket;    

    socket.on("move", move=>{
        switch (move) {
                
            case "up":
                player.posY++; break;
            
            case "down":
                player.posY--; break;
            
            case "left":
                player.posX--; break;
            
            case "right":
                player.posX++; break;
        }
    });
})

function updateMap(){
    map[player.posX][player.posY] = 10;
}

setInterval(function(){
    updateMap();
    for(var i in SOCKET_LIST){
        SOCKET_LIST[i].emit("map", map);
    }
},1000/30)
